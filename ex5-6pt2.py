# Инициализация переменных для суммы и количества чисел
total_sum = 0
count = 0

# Ввод первого числа
number = int(input("Введите число (для завершения введите 0): "))

# Цикл для суммирования и подсчета количества чисел
while number != 0:
    total_sum += number
    count += 1
    number = int(input("Введите число (для завершения введите 0): "))

# Вывод суммы и количества чисел
print("Сумма чисел:", total_sum)
print("Количество чисел:", count)
